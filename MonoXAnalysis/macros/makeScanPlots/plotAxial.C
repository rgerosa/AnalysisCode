static bool addRelicDensity = true;
static float nbinsX = 800;
static float nbinsY = 500;
static float minX = 0;
static float minY = 1.;
static float maxX = 2500;
static float maxY = 1000;
static float minZ = 0.01;
static float maxZ = 20;

